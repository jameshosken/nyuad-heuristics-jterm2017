#Count to 100, and if the number is divisible by 3 add "!" to it

for i in range(101):

	if(i == 3):
		print (i, "!")
	else:
		print (i)



#Count to 100, and if the number is less than 50 add "!" to it

for i in range(101):

	if(i < 50):
		print (i, "!")
	else:
		print (i)
